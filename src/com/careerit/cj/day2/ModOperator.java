package com.careerit.cj.day2;

public class ModOperator {

		public static void main(String[] args) {
			
			int num1 = 9;
			int num2 = 2;
			
			System.out.println(num1 / num2);
			System.out.println(num1 % num2);
			
			System.out.println(num2 / num1);
			System.out.println(num2 % num1);
		}
}
